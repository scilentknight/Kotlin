fun main(args: Array<String>) {
    // var - var can be reassigned with the same type
    var a = 40
    var b = 78.2
    var c = "Coding Boot"
    println(a)

    // val -'val' cannot be reassigned.
    val v = 50
    println(v)

    // : 'colon' for extends
    val e : Double = 20.3
    println(e)

}